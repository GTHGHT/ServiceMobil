    private void btnEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMouseClicked
        // TODO add your handling code here:
        String editServisSQL = "UPDATE cust SET nama = ? , nohp = ? WHERE id_cust = ?";
        try (
            Connection con = ds.getConnection();
            PreparedStatement statement = con.prepareStatement(editServisSQL)
        ){
            statement.setString(3, Id_Cust_Txt.getText());
            statement.setString(1, Nama_Cust_Txt.getText());
            statement.setString(2, No_Handphone_Txt.getText());
            statement.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data Berhasil Diubah");
            Id_Cust_Txt.setText("");
            Nama_Cust_Txt.setText("");
            No_Handphone_Txt.setText("");
        }catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane,
                "Pengubahan Customer Gagal\nKode Error SQL = "+ ex.getSQLState(),
                "Aksi Gagal", JOptionPane.ERROR_MESSAGE);
        }
        loadCustomer();
    }//GEN-LAST:event_btnEditMouseClicked

