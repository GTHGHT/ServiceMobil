    private void btnAddNewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddNewMouseClicked
        // TODO add your handling code here:
        new Tambah_Customer(this).setVisible(true);
        Customer customer = Tambah_Customer.getInput();
        if(!customer.getNamaCustomer().equals("")||!customer.getNoHandphone().equals("")){
            String tambahCustomerSQL = "INSERT INTO cust(nama, nohp) VALUES(?,?)";
            try (Connection con = ds.getConnection();
                PreparedStatement statement = con.prepareStatement(tambahCustomerSQL)){
                statement.setString(1, customer.getNamaCustomer());
                statement.setString(2, customer.getNoHandphone());
                statement.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data Berhasil Tambah");
            }catch (SQLException e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(rootPane,
                    "Penambahan Service Gagal\nKode Error SQL = "+e.getSQLState(),
                    "Aksi Gagal", JOptionPane.ERROR_MESSAGE);
            }
            loadCustomer();
        }
    }//GEN-LAST:event_btnAddNewMouseClicked

